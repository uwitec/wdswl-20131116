	// 取得自由项信息
	public FreeVO getFreevo() {
		if (freevo == null) {
			freevo = new FreeVO();
			String[] names = getAttributeNames();
			if (names != null && names.length > 0) {
				for (String name : names) {
					freevo.setAttributeValue(name, getAttributeValue(name));
				}
			}
		}
		return freevo;
	}

